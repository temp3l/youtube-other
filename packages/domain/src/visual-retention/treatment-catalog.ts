const treatmentCatalogById = new Map<string, ReadonlyTreatmentCatalogEntry>(
  shotTreatmentCatalog.map((catalogEntry) => [catalogEntry.id, catalogEntry]),
);

export function getTreatment(
  id: string,
): ReadonlyTreatmentCatalogEntry | undefined {
  return treatmentCatalogById.get(id);
}

export function isTreatmentSupported(id: string): boolean {
  const treatment = getTreatment(id);
  return treatment !== undefined && treatment.status === "supported";
}

export function isTreatmentValidForAspectRatio(
  id: string,
  aspectRatio: z.infer<typeof aspectRatioSchema>,
): boolean {
  const treatment = getTreatment(id);
  return treatment?.aspectRatios.includes(aspectRatio) ?? false;
}

export function isTreatmentValidForPhase(
  id: string,
  phase: z.infer<typeof visualNarrativePhaseSchema>,
): boolean {
  const treatment = getTreatment(id);
  return treatment?.phases.includes(phase) ?? false;
}

export function isTreatmentValidForDuration(
  id: string,
  durationMs: number,
): boolean {
  const treatment = getTreatment(id);
  return (
    treatment !== undefined &&
    durationMs >= treatment.durationMs.minMs &&
    durationMs <= treatment.durationMs.maxMs
  );
}

export function areTreatmentsCompatible(ids: readonly string[]): boolean {
  const uniqueIds = new Set(ids);
  for (const id of uniqueIds) {
    const treatment = getTreatment(id);
    if (treatment === undefined) {
      return false;
    }
    for (const incompatibleId of treatment.incompatibleTreatmentIds) {
      if (uniqueIds.has(incompatibleId)) {
        return false;
      }
    }
  }
  return true;
}
