import { z } from "zod";
import {
  aspectRatioSchema,
  durationRangeSchema,
  shotTreatmentIdSchema,
  visualNarrativePhaseSchema,
} from "../index.js";

const transitionPhaseSchema = z.enum([
  "hook",
  "setup",
  "evidence",
  "escalation",
  "climax",
  "callback",
  "aftermath",
]);

const treatmentCatalogCategorySchema = z.enum([
  "static-reframing",
  "camera-movement",
  "aspect-ratio-adaptation",
  "depth-and-motion",
  "horror-specific",
  "evidence-related",
  "transition",
]);
export type TreatmentCatalogCategory = z.infer<
  typeof treatmentCatalogCategorySchema
>;

const treatmentCatalogStatusSchema = z.enum([
  "supported",
  "later-phase",
  "unsupported",
]);
export type TreatmentCatalogStatus = z.infer<
  typeof treatmentCatalogStatusSchema
>;

const focalMetadataRequirementSchema = z.enum(["none", "optional", "required"]);
export type FocalMetadataRequirement = z.infer<
  typeof focalMetadataRequirementSchema
>;

const overlayAssetRequirementSchema = z.enum([
  "none",
  "local-overlay-asset",
  "external-overlay-asset",
]);
export type OverlayAssetRequirement = z.infer<
  typeof overlayAssetRequirementSchema
>;

const treatmentCompatibilityTagSchema = z.enum([
  "caption-safe",
  "face-sensitive",
  "evidence-readable",
  "fallback-only",
  "text-heavy-risk",
  "low-resolution-risk",
  "crowded-scene-risk",
  "perspective-distortion-risk",
  "overlay-heavy",
]);
export type TreatmentCompatibilityTag = z.infer<
  typeof treatmentCompatibilityTagSchema
>;

const treatmentCatalogEntrySchema = z.object({
  id: shotTreatmentIdSchema,
  label: z.string().min(1),
  category: treatmentCatalogCategorySchema,
  aspectRatios: z.array(aspectRatioSchema).min(1),
  phases: z.array(visualNarrativePhaseSchema).min(1),
  durationMs: durationRangeSchema,
  inlineRenderable: z.boolean(),
  derivedClipCacheRequired: z.boolean(),
  status: treatmentCatalogStatusSchema,
  availableByDefault: z.boolean(),
  focalMetadataRequirement: focalMetadataRequirementSchema,
  overlayAssetRequirement: overlayAssetRequirementSchema,
  compatibilityTags: z.array(treatmentCompatibilityTagSchema),
  incompatibleTreatmentIds: z.array(shotTreatmentIdSchema),
  frequencyCapRefs: z.array(z.string().min(1)),
});
type TreatmentCatalogEntryInput = z.input<typeof treatmentCatalogEntrySchema>;
type TreatmentCatalogEntry = z.output<typeof treatmentCatalogEntrySchema>;

export const shotTreatmentCatalogVersion = "shot-treatment-catalog-v1";

const allPhases: z.infer<typeof visualNarrativePhaseSchema>[] = [
  "hook",
  "setup",
  "evidence",
  "escalation",
  "climax",
  "callback",
  "aftermath",
];

function freezeEntry(entry: TreatmentCatalogEntry) {
  return Object.freeze({
    ...entry,
    aspectRatios: Object.freeze([...entry.aspectRatios]),
    phases: Object.freeze([...entry.phases]),
    compatibilityTags: Object.freeze([...entry.compatibilityTags]),
    incompatibleTreatmentIds: Object.freeze([...entry.incompatibleTreatmentIds]),
    frequencyCapRefs: Object.freeze([...entry.frequencyCapRefs]),
  });
}

function entry(
  value: TreatmentCatalogEntryInput,
) {
  return freezeEntry(treatmentCatalogEntrySchema.parse(value));
}

export type ReadonlyTreatmentCatalogEntry = ReturnType<typeof entry>;

